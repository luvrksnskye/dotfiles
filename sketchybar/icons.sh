# ═══════════════════════════════════════════════════════════════════════
#                    APP ICON MAPPING
#          (Used by front_app for dynamic icons)
# ═══════════════════════════════════════════════════════════════════════

get_app_icon() {
    local app_name=$(echo "$1" | tr '[:upper:]' '[:lower:]')
    
    case "$app_name" in
        *terminal*|*iterm*|*alacritty*|*kitty*|*wezterm*|*warp*)
            echo "$ICON_TERMINAL"
            ;;
        *code*|*vscode*|*cursor*|*zed*|*sublime*|*atom*|*vim*|*nvim*|*neovim*)
            echo "$ICON_CODE"
            ;;
        *chrome*)
            echo "$ICON_CHROME"
            ;;
        *safari*)
            echo "$ICON_SAFARI"
            ;;
        *firefox*)
            echo "$ICON_FIREFOX"
            ;;
        *arc*)
            echo "$ICON_ARC"
            ;;
        *brave*|*edge*|*opera*|*vivaldi*)
            echo "$ICON_BROWSER"
            ;;
        *finder*)
            echo "$ICON_FINDER"
            ;;
        *spotify*)
            echo "$ICON_SPOTIFY"
            ;;
        *music*|*itunes*)
            echo "$ICON_APPLE_MUSIC"
            ;;
        *youtube*)
            echo "$ICON_YOUTUBE"
            ;;
        *messages*|*imessage*)
            echo "$ICON_MESSAGES"
            ;;
        *slack*)
            echo "$ICON_SLACK"
            ;;
        *discord*)
            echo "$ICON_DISCORD"
            ;;
        *telegram*)
            echo "$ICON_TELEGRAM"
            ;;
        *figma*)
            echo "$ICON_FIGMA"
            ;;
        *github*|*gitkraken*|*sourcetree*)
            echo "$ICON_GITHUB"
            ;;
        *notes*)
            echo "$ICON_NOTES"
            ;;
        *mail*|*outlook*|*spark*|*airmail*)
            echo "$ICON_MAIL"
            ;;
        *)
            echo "$ICON_WINDOW"
            ;;
    esac
}
